function add7(num) {
    return num + 7;
}

function multiply(a, b) {
    return a * b;
}

function capitalize(string) {
    return string.at(0).toUpperCase() + string.slice(1);
}

function lastLetter(string) {
    return string.at(-1);
}

const add = function(a, b) {
    return a + b;
};

const subtract = (a, b) => {
    return a - b;
}